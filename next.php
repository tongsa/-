function oneValues(){
var result;
var url=window.location.search; //获取url中"?"符后的字串  
if(url.indexOf("?")!=-1){
result = url.substr(url.indexOf("=")+1);
}

if(result>=8){
        	$('.s1').show();
        	$('.s2').hide();
			$('.s3').hide();
			$('.share1').show();
			$('.share').hide();
			
        }
        //小于6的时候就显示另一个文本
        else if(result>=4){
        	$('.s2').show();
        	$('.s1').hide();
			$('.s3').hide();
			$('.share').show();
			$('.share1').hide();
        }
		else if(result>=0){
        	$('.s3').show();
        	$('.s1').hide();
			$('.s2').hide();
			$('.share').show();
			$('.share1').hide();
        }
return result;
}

